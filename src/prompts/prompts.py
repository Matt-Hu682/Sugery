def get_prompt(task_type, mode="single"):
    if task_type == "Door":
        if mode == "single":
            return """
                請觀察大門。

                請判斷：大門目前是「打開的」還是「關著的」？

                【輸出 1 的條件：門是打開的】
                - 打開的門看的到外面的景象。

                【輸出 0 的條件：門是關著的】
                - 大門是完全緊閉。
                """
        if mode == "single_bed":  # 單幀裁切圖（靜態畫面）
            return """
                請觀察這張靜態畫面，判斷是否有「有載人推床或輪椅」存在。

                【輸出 1：有推床】
                - 條件 A（床或輪椅可見）：畫面中出現「有病人躺著的推床或有人坐的輪椅」。
                - 條件 B（床被遮住）：綠色衣服人員圍在推床周圍，且符合以下全部條件：
                條件 A 或 B 任一成立即輸出 1。

                【輸出 0：沒有載人的推床輪椅】
                - 只有器械車、螢幕車、方形推車、空金屬護欄，輸出 0。

                請只回答數字：0 或 1
                """

        if mode == "video_cross":  # 裁切圖
            return """
            你會看到一段按時間排序的畫面（第1張是起點，第12張是終點）。
            只回答一個數字：0 或 1。

            任務：
            判斷是否有「載人推床或載人輪椅」穿越手術室大門。

            重要空間定義：
            - 手術室內部：畫面前景的房間內區域。
            - 門外/走廊：門框後方、窗戶前方的區域。
            - 門檻：門框所在的邊界線。只有物體從手術室內部跨到門外/走廊，或從門外/走廊跨到手術室內部，才算穿越。

            先做目標判斷（最重要）：
            - 可當作「載人」：推床上有綠色棉被覆蓋人體的形狀（即使頭部被遮擋）。
            - 不是載人的推床或輪椅：儀器車、置物車、空金屬床架、只有布遮蓋。
            - 如果目標不是有載人推床或有坐人的輪椅，直接輸出 0。

            輸出 1 必須同時成立：
            1. 同一台有載人推床或有載人輪椅在連續畫面中被推行。
            2. 目標的主體位置從「手術室內部」變成「門外/走廊」，或從「門外/走廊」變成「手術室內部」。
            3. 目標必須跨越門檻線，不只是出現在門框後方。
            4. 終點時目標已大部分離開原本所在區域。

            以下情況一律輸出 0：
            - 推床只是在門外/走廊側橫向經過門口視野。
            - 推床只從左到右或右到左經過門框後方，但沒有進入手術室內部。
            - 目標只在門口附近移動，沒有跨越門檻。
            - 只有人員進出，沒有可確認的載人推床/輪椅。
            - 被推過的是儀器車、置物車、空床架或空輪椅。
            - 不確定時輸出 0。

            最後只能回答：0 或 1
            """

        if mode == "patient_check":  # 全圖
            return """
                已知這段影像中發生了推床或輪椅穿過手術室大門的事件。

                請仔細觀察畫面中的手術台（通常位於畫面中央偏左）：

                輸出 1：手術台上有病人躺著
                輸出 0：手術台是空的，沒有病人，或看不清楚

                只回答一個數字：0 或 1
                """

    if task_type == "Surgery":
        return """
                請觀察圖片中間的床。

                請判斷：【是否有一群「站著的人」圍繞著一個「手術台」?】

                判斷規則：
                1. 如果看到手術台床上躺著一個人，有綠色的棉被蓋住，輸出 1。
                2. 動作條件：必須有 2 位 或 2位以上的「站著的人」 圍在手術台旁邊，且他們的方向是朝向床上的 輸出1。

                【排除條件 (輸出 0)】：
                    - 空床：床上沒人，無論旁邊有多少人，都輸出 0。
                    - 黑色mask: 如果畫面中手術台有「黑色色塊」部分區域遮罩，並沒看到人躺在床上的(需有粉紅色帽子)，請一律輸出 0。
                    - 單人/散開：只有 1 個人在床邊，或是多人但分散在房間各處，輸出 0。

                若符合「圍繞」狀態，輸出: 1
                有人躺在手術台上，輸出: 1
                若不符合，輸出: 0

                請只回答數字。
                """

    return ""
